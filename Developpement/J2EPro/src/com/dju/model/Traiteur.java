package com.dju.model;

import java.util.*;

public class Traiteur
{
    public Traiteur()
    {
    }

    private int id_traiteur;

    private String nom_traiteur;

	public int getId_traiteur() {
		return id_traiteur;
	}

	public void setId_traiteur(int id_traiteur) {
		this.id_traiteur = id_traiteur;
	}

	public String getNom_traiteur() {
		return nom_traiteur;
	}

	public void setNom_traiteur(String nom_traiteur) {
		this.nom_traiteur = nom_traiteur;
	}
}